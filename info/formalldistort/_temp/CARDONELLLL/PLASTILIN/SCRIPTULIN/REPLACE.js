function replikant(){
var i;
var j;
var k;
var nawel;
var doptext;
var mimo;
var z=1;
var propustil = 0;
var zapas = '';
var filtr;
var necifra;
var cifra;
var propusk;
var peremena;
var peremenajaot = parseFloat(document.getElementById('iperemenajaot').value);
var peremenajado= parseFloat(document.getElementById('iperemenajado').value);
var peremenajawag = parseFloat(document.getElementById('iperemenajawag').value);
var peremenaja;
var peredel='';
var zamena = '';
var zapros ='|' + document.getElementById('izapros').value;
var fragment = document.getElementById('okno2').value.split('')
var textposle='';


if (!nazad){
document.getElementById('iwagnazad').value=document.getElementById('okno2').value; nazad=true;
}

 filtr = "0123456789"; filtr= filtr.split('');

if  (document.getElementById('czapros_necifra').checked){
    necifra=true
    }else{necifra=false}

if  (document.getElementById('czapros_cifra').checked){
    cifra=true
    }else{cifra=false}

if (document.getElementById('czamena').checked){
zamena = document.getElementById('izamena').value
}else{zamena = document.getElementById('izapros').value}

zapros = zapros.split('');

if (document.getElementById('cpropusk').checked){
propusk = true;
}else{propusk=false}

if (document.getElementById('cperemenaja').checked){
peremenaja=parseFloat(document.getElementById('iperemenajaot').value); peremena=true
}else{peremena=false}

if (document.getElementById('ctextposle').checked){
textposle = document.getElementById('itextposle').value; doptext=true
}

for (i=0;i<fragment.length;i++){
    
    if (fragment[i]==zapros[z]){
        
        if (z+1<zapros.length && z==1){z++; zapas=fragment[i]}
        else if (z+1<zapros.length && z>1){z++}
        else if (z+1==zapros.length){nawel=true; peredel+=zamena; z=1}
                
        }
    else if (fragment[i]!=zapros[z]){
        
        if (z>1){peredel+=zapas; i-=z-1; z=1}
        else if (z==1){peredel+=fragment[i]}
        
        }
        
    if (nawel){
        
        if (cifra || necifra){
            
            for (k=0; k<filtr.length; k++){
                if (cifra){if (fragment[i+1]==filtr[k]){nawel=true}}
                else if (necifra){if (fragment[i+1]==filtr[k]){nawel=false}}
                }
            
            }
        
        }
        
    if (nawel && propusk){

        for (j=i+1;j<fragment.length;j++){
            
            for (k=0;k<filtr.length;k++){
                
                if (fragment[j]==filtr[k]){peredel+=fragment[j]; mimo=false; propustil++; break;}
                else if (fragment[j]!=filtr[k]){mimo=true;}
                
                }
            if (mimo){break}
            
            }
        i+=propustil; propustil=0
        
        }
    if (nawel && peremena && peremenaja <= peremenajado){peredel+=peremenaja; peremenaja+=peremenajawag}
    if (nawel && doptext){peredel+=textposle}
    if (nawel){nawel=false}
    
    }

document.getElementById('okno2').value = peredel;

}

